package controller;

public class evalAptBean {
	
	private String textEval;
	private Integer stars;
	private String status;
	private String aptId;
	private String owner;
	private String evalUsr;
	

	public String getTextEval() {
		return textEval;
	}
	public void setTextEval(String textEval) {
		this.textEval = textEval;
	}
	
	public Integer getStars() {
		return stars;
	}
	public void setStars(Integer stars) {
		this.stars = stars;
	}
	
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	public String getAptId() {
		return aptId;
	}
	public void setAptId(String aptId) {
		this.aptId = aptId;
	}
	
	public String getOwner() {
		return owner;
	}
	public void setOwner(String owner) {
		this.owner = owner;
	}
	
	public String getEvalUsr() {
		return evalUsr;
	}
	public void setEvalUsr(String evalUsr) {
		this.evalUsr = evalUsr;
	}

}
