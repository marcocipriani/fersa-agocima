package controller;

public class evalUsrBean {
	
	private String textEval;
	private Integer stars;
	private String status;
	private String nickname;
	private String evalUsr;
	

	public String getTextEval() {
		return textEval;
	}
	public void setTextEval(String textEval) {
		this.textEval = textEval;
	}
	
	public Integer getStars() {
		return stars;
	}
	public void setStars(Integer stars) {
		this.stars = stars;
	}
	
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	
	public String getEvalUsr() {
		return evalUsr;
	}
	public void setEvalUsr(String evalUsr) {
		this.evalUsr = evalUsr;
	}

}
